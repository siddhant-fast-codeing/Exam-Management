    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                       
    
    try {
        DefaultTableModel tblModel = (DefaultTableModel) table.getModel();
        int selectedRow = table.getSelectedRow();
        
        if (selectedRow != -1) {
            String registrationNo = (String) tblModel.getValueAt(selectedRow, 0); // Change int to String
            stmt = conn.createStatement();
            
            // Delete record from database
            String deleteQuery = "DELETE FROM add_addmission WHERE Registration_Number = '" + registrationNo + "'";
            String deleteQuery1 = "DELETE FROM student_user_id_password WHERE Registration_Number = '" + registrationNo + "'";
            stmt.executeUpdate(deleteQuery);
            stmt.executeUpdate(deleteQuery1);
            
            // Remove row from table model
            tblModel.removeRow(selectedRow);
            
            JOptionPane.showMessageDialog(null, "Record deleted successfully.");
        } else {
            JOptionPane.showMessageDialog(null, "Select a row to delete.");
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    
    // Filter records based on name in the text field
    filterTable(search.getText());



    }//GEN-LAST:event_jButton1ActionPerformed

