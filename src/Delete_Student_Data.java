    private void filterTable(String name) {
    DefaultTableModel tblModel = (DefaultTableModel) table.getModel();
    tblModel.setRowCount(0); // Clear the table
    
    try {
        stmt = conn.createStatement();
        String query = "SELECT * FROM add_addmission WHERE First_Name = '" + name + "'";

        rs = stmt.executeQuery(query);
        
        while (rs.next()) {
            String registrationNo = String.valueOf(rs.getInt("Registration_Number"));
            String studentName = rs.getString("Name_On_Certificate");
            String Corse_Name = rs.getString("Course_Name");
            String Gender = rs.getString("Gender");
            String email = rs.getString("Email_ID");
            String form_date = rs.getString("Date_Of_Admission");
            
            // ... (add other columns as needed)
            
            tblModel.addRow(new Object[]{registrationNo, studentName,Corse_Name,Gender,email,form_date});
        }
        
      
    } catch (SQLException e) {
        e.printStackTrace();
    }
}

